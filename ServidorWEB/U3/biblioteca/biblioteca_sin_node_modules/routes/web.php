Route::get(
    'saludo/{nombre?}/{id?}',
    function ($nombre = "Invitado", $id = 0) {
        return "Hola tu nombre es $nombre, tu código es $id ";
    }
)->where('nombre', "[A-Za-z]+")
    ->where('id', "[0-9]+");

